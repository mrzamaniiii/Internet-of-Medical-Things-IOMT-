def multi_notch_filter(data, fs, freqs=(50.0, 100.0, 150.0), Q=35.0):
    """
    Apply multiple narrow notch filters to remove power-line hum
    and its harmonics.
    """
    x = np.asarray(data, dtype=np.float64).copy()
    nyq = 0.5 * fs

    for f in freqs:
        if f <= 0:
            continue

        if f < nyq - 1.0:
            w0 = f / nyq
            b, a = iirnotch(w0, Q)
            x = filtfilt(b, a, x)

    return x


def bandpass_filter(data, lowcut, highcut, fs, order=4):
    """
    Zero-phase Butterworth bandpass filter.
    """
    x = np.asarray(data, dtype=np.float64)
    nyq = 0.5 * fs

    effective_low = max(float(lowcut), 0.1)
    effective_high = min(float(highcut), nyq - 1.0)

    if effective_low >= effective_high:
        raise ValueError(
            f"Invalid bandpass range: lowcut={effective_low}, highcut={effective_high}, nyquist={nyq}"
        )

    b, a = butter(
        order,
        [effective_low / nyq, effective_high / nyq],
        btype="band"
    )

    return filtfilt(b, a, x)


def wavelet_denoise(data, wavelet="db6", level=4, thresh_scale=0.6):
    """
    Wavelet denoising using soft-thresholding.
    The approximation coefficients are preserved.
    """
    x = np.asarray(data, dtype=np.float64)
    original_len = len(x)

    if original_len < 16:
        return x.copy()

    max_level = pywt.dwt_max_level(
        data_len=original_len,
        filter_len=pywt.Wavelet(wavelet).dec_len
    )

    effective_level = min(level, max_level)

    if effective_level < 1:
        return x.copy()

    coeffs = pywt.wavedec(
        x,
        wavelet,
        level=effective_level
    )

    sigma = np.median(np.abs(coeffs[-1])) / 0.6745

    if sigma < 1e-12:
        return x.copy()

    threshold = thresh_scale * sigma * np.sqrt(2 * np.log(original_len))

    coeffs_thresholded = [coeffs[0]]

    for c in coeffs[1:]:
        coeffs_thresholded.append(
            pywt.threshold(
                c,
                value=threshold,
                mode="soft"
            )
        )

    denoised = pywt.waverec(
        coeffs_thresholded,
        wavelet
    )

    denoised = denoised[:original_len]

    if len(denoised) < original_len:
        denoised = np.pad(
            denoised,
            (0, original_len - len(denoised)),
            mode="edge"
        )

    return denoised


