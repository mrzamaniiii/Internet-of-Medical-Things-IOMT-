def extract_aligned_cycle_snippets(cycles, signal, sr, pre_s=0.10, post_s=None):
    if len(cycles) == 0:
        return None

    cycle_durations_s = np.array(
        [c["cycle_duration_ms"] / 1000.0 for c in cycles],
        dtype=float
    )

    if post_s is None:
        post_s = np.median(cycle_durations_s)
        post_s = np.clip(post_s, 0.60, 1.20)

    pre = int(pre_s * sr)
    post = int(post_s * sr)
    win_len = pre + post

    snippets = []
    valid_cycle_indices = []

    for ci, c in enumerate(cycles):
        s1_start = int(c["_s1_start"])

        start = s1_start - pre
        end = s1_start + post

        if start < 0 or end > len(signal):
            continue

        snippet = np.asarray(signal[start:end], dtype=np.float64)

        if len(snippet) != win_len:
            continue

        snippet = snippet - np.mean(snippet)
        snippet = snippet / (np.std(snippet) + 1e-10)

        snippets.append(snippet)
        valid_cycle_indices.append(ci)

    if len(snippets) == 0:
        return None

    snippets = np.vstack(snippets)

    time_template = np.arange(-pre, post) / sr

    return {
        "snippets": snippets,
        "valid_cycle_indices": np.array(valid_cycle_indices, dtype=int),
        "time_template": time_template,
        "pre_s": pre_s,
        "post_s": post_s,
    }


