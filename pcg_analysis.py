def segment_rms(x):
    x = np.asarray(x, dtype=np.float64)

    if len(x) == 0:
        return 0.0

    return float(np.sqrt(np.mean(x ** 2)))


def split_into_thirds(x):
    x = np.asarray(x, dtype=np.float64)
    n = len(x)

    if n < 6:
        return [
            np.array([], dtype=np.float64),
            np.array([], dtype=np.float64),
            np.array([], dtype=np.float64),
        ]

    i1 = n // 3
    i2 = 2 * n // 3

    return [
        x[:i1],
        x[i1:i2],
        x[i2:],
    ]


def energy_fractions_three_parts(x):
    parts = split_into_thirds(x)

    energies = np.array(
        [
            np.sum(part ** 2)
            for part in parts
        ],
        dtype=float
    )

    total_energy = np.sum(energies) + 1e-12

    return energies / total_energy


def local_rms_ratios_three_parts(x, reference_rms):
    parts = split_into_thirds(x)

    ratios = np.array(
        [
            segment_rms(part) / (reference_rms + 1e-10)
            for part in parts
        ],
        dtype=float
    )

    return ratios


def envelope_peak_position(x):
    x = np.asarray(x, dtype=np.float64)

    if len(x) < 8:
        return np.nan

    env = uniform_filter1d(
        np.abs(hilbert(x)),
        size=max(3, len(x) // 5)
    )

    return float(np.argmax(env) / max(1, len(env) - 1))


