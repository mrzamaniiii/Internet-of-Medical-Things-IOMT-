def band_power_fft(x, sr, f_low, f_high):
    x = np.asarray(x, dtype=np.float64)
    x = x - np.mean(x)

    freqs = np.fft.rfftfreq(len(x), d=1.0 / sr)
    spectrum = np.fft.rfft(x)
    power = np.abs(spectrum) ** 2

    mask = (freqs >= f_low) & (freqs <= f_high)

    if not np.any(mask):
        return 0.0

    return float(np.sum(power[mask]))


def spectral_features(x, sr, f_low=0.0, f_high=None):
    x = np.asarray(x, dtype=np.float64)
    x = x - np.mean(x)

    if f_high is None:
        f_high = sr / 2.0

    freqs = np.fft.rfftfreq(len(x), d=1.0 / sr)
    spectrum = np.fft.rfft(x)
    power = np.abs(spectrum) ** 2

    mask = (freqs >= f_low) & (freqs <= f_high)
    freqs = freqs[mask]
    power = power[mask]

    total_power = np.sum(power) + 1e-12

    dominant_freq = float(freqs[np.argmax(power)])
    centroid = float(np.sum(freqs * power) / total_power)

    bandwidth = float(
        np.sqrt(
            np.sum(((freqs - centroid) ** 2) * power) / total_power
        )
    )

    p = power / total_power
    entropy = float(
        -np.sum(p * np.log2(p + 1e-12)) / np.log2(len(p) + 1e-12)
    )

    return {
        "dominant_freq_hz": dominant_freq,
        "spectral_centroid_hz": centroid,
        "spectral_bandwidth_hz": bandwidth,
        "spectral_entropy": entropy,
        "total_power": float(total_power),
    }


