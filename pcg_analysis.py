"""---
## Module 3: Murmur Detection & Grading

Analyzes energy during systolic and diastolic intervals.  
Normal hearts have near-silence between S1 and S2. Murmurs appear as sustained sound.

**Detectable at 250 Hz Nyquist:**
- Aortic stenosis (80–200 Hz, diamond-shaped systolic murmur)
- Mitral regurgitation components (100–200 Hz)
- Timing classification (systolic vs diastolic)

**Grading (Levine scale adaptation):**

| Grade | Description | Noise Ratio |
|-------|-------------|-------------|
| 0 | None | < 0.15 |
| 1 | Barely detectable | 0.15–0.30 |
| 2 | Faint but clear | 0.30–0.50 |
| 3 | Moderately loud | 0.50–0.70 |
| 4 | Loud | 0.70–0.90 |
| 5–6 | Very loud | > 0.90 |
"""

# ==================================================
# MURMUR-LIKE ACTIVITY DETECTION FUNCTIONS
# ==================================================

def murmur_like_grade(ratio):
    ratio = float(ratio)

    for i, th in enumerate(MURMUR_GRADE_THRESHOLDS):
        if ratio < th:
            return i

    return 6


def detect_murmur_like_activity(filtered, cycle, sr):
    sys_sig = filtered[int(cycle["_sys_start"]):int(cycle["_sys_end"])]
    dia_sig = filtered[int(cycle["_dia_start"]):int(cycle["_dia_end"])]

    s1_rms = float(cycle.get("s1_rms", 0.0))
    s2_rms = float(cycle.get("s2_rms", 0.0))

    reference_rms = 0.5 * (s1_rms + s2_rms)

    result = {
        "systolic_murmur_like": False,
        "diastolic_murmur_like": False,
        "systolic_grade": 0,
        "diastolic_grade": 0,
        "systolic_ratio": 0.0,
        "diastolic_ratio": 0.0,
        "sys_diamond": False,
        "dia_decrescendo": False,
        "reference_rms": float(reference_rms),
    }

    if reference_rms < 1e-8:
        return result

    # --------------------------------------------------
    # Systolic murmur-like activity
    # --------------------------------------------------
    if len(sys_sig) > 4:
        sys_rms = rms(sys_sig)
        sys_ratio = float(sys_rms / (reference_rms + 1e-10))

        result["systolic_ratio"] = sys_ratio
        result["systolic_grade"] = murmur_like_grade(sys_ratio)

        result["systolic_murmur_like"] = bool(
            sys_ratio >= MURMUR_DETECTION_RATIO
        )

        if len(sys_sig) > 15:
            sys_env = uniform_filter1d(
                np.abs(hilbert(sys_sig)),
                size=max(3, len(sys_sig) // 5)
            )

            peak_position = np.argmax(sys_env) / max(1, len(sys_env) - 1)

            result["sys_diamond"] = bool(
                result["systolic_murmur_like"] and
                0.25 < peak_position < 0.75
            )

    # --------------------------------------------------
    # Diastolic murmur-like activity
    # --------------------------------------------------
    if len(dia_sig) > 4:
        dia_rms = rms(dia_sig)
        dia_ratio = float(dia_rms / (reference_rms + 1e-10))

        result["diastolic_ratio"] = dia_ratio
        result["diastolic_grade"] = murmur_like_grade(dia_ratio)

        result["diastolic_murmur_like"] = bool(
            dia_ratio >= MURMUR_DETECTION_RATIO
        )

        if len(dia_sig) > 15:
            dia_env = uniform_filter1d(
                np.abs(hilbert(dia_sig)),
                size=max(3, len(dia_sig) // 5)
            )

            q1 = np.mean(dia_env[:len(dia_env) // 4])
            q4 = np.mean(dia_env[3 * len(dia_env) // 4:])

            result["dia_decrescendo"] = bool(
                result["diastolic_murmur_like"] and
                q4 > 0 and
                q1 > 1.5 * q4
            )

    return result


print("✓ Murmur-like activity detection functions defined")

