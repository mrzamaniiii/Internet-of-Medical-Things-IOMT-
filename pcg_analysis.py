        print(f"Median HR:            {raw_metrics['median_hr']:.1f} BPM")

        print()
        print("Artifact-rejected S1-S1 intervals")
        print("-" * 70)
        print(f"Median-based range:   {lower_limit:.1f}–{upper_limit:.1f} ms")
        print(f"Intervals kept:       {len(rr_clean)} / {len(rr_basic)}")
        print(f"Intervals rejected:   {len(rejected_rr)}")

