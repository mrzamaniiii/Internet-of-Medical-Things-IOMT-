"""---
## Heart Sound Segmentation

Segments the signal into four cardiac phases per cycle:  
**S1 → Systole → S2 → Diastole → S1 → ...**

Uses envelope width at 40% of peak height to estimate S1/S2 boundaries,  
then fills systole and diastole in the gaps.
"""

# ==================================================
# SEGMENTATION FUNCTIONS — FOUR STATES ONLY
# ==================================================

def estimate_sound_width(envelope, peak_idx, sr, max_width_s=0.15, relative_height=0.4):
    peak_idx = int(peak_idx)
    half_height = envelope[peak_idx] * relative_height
    max_w = int(sr * max_width_s)

    left = peak_idx
    for j in range(peak_idx, max(0, peak_idx - max_w), -1):
        if envelope[j] < half_height:
            left = j
            break

    right = peak_idx
    for j in range(peak_idx, min(len(envelope), peak_idx + max_w)):
        if envelope[j] < half_height:
            right = j
            break

    return left, right


def build_sound_bounds(envelope, peaks, sr, max_width_s, relative_height=0.4):
    bounds = []

    for pk in peaks:
        left, right = estimate_sound_width(
            envelope,
            pk,
            sr,
            max_width_s=max_width_s,
            relative_height=relative_height
        )

        if right > left:
            bounds.append((int(pk), int(left), int(right)))

    return sorted(bounds, key=lambda x: x[0])


def segment_heart_sounds(filtered, envelope, s1_peaks, s2_peaks, sr, cycle_pairs=None):
    n = len(filtered)

    # 0=S1, 1=Systole, 2=S2, 3=Diastole
    states = np.full(n, 3, dtype=int)

    s1_bounds = build_sound_bounds(
        envelope,
        s1_peaks,
        sr,
        max_width_s=0.08,
        relative_height=0.4
    )

    s2_bounds = build_sound_bounds(
        envelope,
        s2_peaks,
        sr,
        max_width_s=0.07,
        relative_height=0.4
    )

    s1_bound_map = {pk: (left, right) for pk, left, right in s1_bounds}
    s2_bound_map = {pk: (left, right) for pk, left, right in s2_bounds}

    # Mark S1 and S2 sounds
    for _, left, right in s1_bounds:
        states[left:right] = 0

    for _, left, right in s2_bounds:
        states[left:right] = 2

    if cycle_pairs is not None and len(cycle_pairs) > 0:
        pair_list = []

        for pair in cycle_pairs:
            if isinstance(pair, dict):
                s1_pk = int(pair["s1_peak"])
                s2_pk = int(pair["s2_peak"])
            else:
                s1_pk = int(pair[0])
                s2_pk = int(pair[1])

            if s1_pk in s1_bound_map and s2_pk in s2_bound_map:
                pair_list.append((s1_pk, s2_pk))

        pair_list = sorted(pair_list, key=lambda x: x[0])

        for i, (s1_pk, s2_pk) in enumerate(pair_list):
            s1_left, s1_right = s1_bound_map[s1_pk]
            s2_left, s2_right = s2_bound_map[s2_pk]

            systole_gap_s = (s2_left - s1_right) / sr

            if 0.03 <= systole_gap_s <= 0.55:
                states[s1_right:s2_left] = 1

            if i + 1 < len(pair_list):
                next_s1_pk, _ = pair_list[i + 1]

                if next_s1_pk in s1_bound_map:
                    next_s1_left, _ = s1_bound_map[next_s1_pk]

                    diastole_gap_s = (next_s1_left - s2_right) / sr

                    if 0.08 <= diastole_gap_s <= 1.30:
                        states[s2_right:next_s1_left] = 3

    else:
        s1_simple_bounds = [(left, right) for _, left, right in s1_bounds]
        s2_simple_bounds = [(left, right) for _, left, right in s2_bounds]

        for _, s1_end in s1_simple_bounds:
            next_s2 = [s2_l for s2_l, _ in s2_simple_bounds if s2_l > s1_end]

            if next_s2:
                gap_s = (next_s2[0] - s1_end) / sr

                if 0.05 <= gap_s <= 0.50:
                    states[s1_end:next_s2[0]] = 1

        for _, s2_end in s2_simple_bounds:
            next_s1 = [s1_l for s1_l, _ in s1_simple_bounds if s1_l > s2_end]

            if next_s1:
                gap_s = (next_s1[0] - s2_end) / sr

                if 0.10 <= gap_s <= 1.30:
                    states[s2_end:next_s1[0]] = 3

    state_names = ["S1", "Systole", "S2", "Diastole"]

    segments = []
    i = 0

    while i < n:
        j = i

        while j < n and states[j] == states[i]:
            j += 1

        segments.append((state_names[int(states[i])], i, j))
        i = j

    return states, segments


