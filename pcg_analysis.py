def lowpass_filter(data, cutoff, fs, order=2):
    nyq = 0.5 * fs
    effective_cutoff = min(cutoff, nyq - 1.0)

    if effective_cutoff <= 0:
        raise ValueError(f"Invalid lowpass cutoff: {cutoff}")

    b, a = butter(order, effective_cutoff / nyq, btype="low")
    return filtfilt(b, a, data)


def shannon_envelope(signal, sr, cutoff=8.0):
    x = np.asarray(signal, dtype=np.float64)
    x = x / (np.max(np.abs(x)) + 1e-10)

    eps = 1e-10
    shannon_energy = -(x ** 2) * np.log((x ** 2) + eps)

    envelope = lowpass_filter(
        shannon_energy,
        cutoff=cutoff,
        fs=sr,
        order=2
    )

    envelope = np.maximum(envelope, 0)
    envelope = envelope / (np.max(envelope) + 1e-10)

    return envelope


